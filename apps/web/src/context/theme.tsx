import { PropsWithChildren } from 'react';
import {
  createTheme,
  Loader,
  MantineProvider as MMantineProvider,
  Modal,
  MultiSelect,
  ScrollArea,
  Select,
} from '@mantine/core';
import { ModalsProvider } from '@mantine/modals';
import { Notifications } from '@mantine/notifications';

const selectProps = {
  searchable: true,
  clearable: true,
} as const;

export const theme = createTheme({
  components: {
    Modal: Modal.extend({
      defaultProps: {
        classNames: { title: 'font-semibold' },
        closeOnEscape: false,
        closeOnClickOutside: false,
      },
    }),
    Loader: Loader.extend({
      defaultProps: {
        type: 'dots',
        size: 'xs',
      },
    }),
    Select: Select.extend({
      defaultProps: selectProps,
    }),
    MultiSelect: MultiSelect.extend({
      defaultProps: selectProps,
    }),
    ScrollArea: ScrollArea.extend({
      defaultProps: {
        scrollbarSize: '0.45rem',
      },
    }),
    ScrollAreaAutosize: ScrollArea.Autosize.extend({
      defaultProps: {
        scrollbarSize: '0.45rem',
      },
    }),
  },
});

export default function ThemeProvider({ children }: PropsWithChildren) {
  return (
    <MMantineProvider theme={theme}>
      <Notifications position='top-right' />
      <ModalsProvider labels={{ cancel: 'Cancel', confirm: 'Confirm' }}>{children}</ModalsProvider>
    </MMantineProvider>
  );
}
